# ── Forgot password ────────────────────────────────────────────────────────────

@router.post("/forgot-password", status_code=200)
def forgot_password(
    data: ForgotPasswordRequest,
    db: Session = Depends(get_db),
):
    """
    Request a password reset email.

    Always returns 200 regardless of whether the email exists — this prevents
    user enumeration attacks (an attacker cannot tell if an email is registered).
    The reset link is valid for 1 hour and is single-use.
    """
    user = db.query(User).filter(User.email == data.email).first()

    if user and user.is_active:
        # Invalidate any existing unused tokens for this user
        db.query(PasswordResetToken).filter(
            PasswordResetToken.user_id == user.id,
            PasswordResetToken.is_used == False,  # noqa: E712
        ).update({"is_used": True})

        raw_token = secrets.token_urlsafe(48)
        expires_at = datetime.now(timezone.utc) + timedelta(
            minutes=settings.PASSWORD_RESET_EXPIRE_MINUTES
        )
        reset_token = PasswordResetToken(
            user_id=user.id,
            token=raw_token,
            expires_at=expires_at,
        )
        db.add(reset_token)
        db.commit()

        email_service.send_password_reset_email(user.email, user.full_name, raw_token)

    # Always return the same message — prevents email enumeration
    return {
        "message": "If that email is registered, a password reset link has been sent."
    }


# ── Reset password ─────────────────────────────────────────────────────────────

@router.post("/reset-password", status_code=200)
def reset_password(
    data: ResetPasswordRequest,
    db: Session = Depends(get_db),
):
    """
    Reset password using the token from the email link.
    The token is single-use and expires after 1 hour.
    """
    now = datetime.now(timezone.utc)

    reset_token = (
        db.query(PasswordResetToken)
        .filter(PasswordResetToken.token == data.token)
        .first()
    )

    if not reset_token:
        raise HTTPException(status_code=400, detail="Invalid or expired reset token.")

    expires_at = reset_token.expires_at
    if expires_at.tzinfo is None:
        expires_at = expires_at.replace(tzinfo=timezone.utc)

    if reset_token.is_used:
        raise HTTPException(status_code=400, detail="This reset link has already been used.")
    if expires_at < now:
        raise HTTPException(status_code=400, detail="This reset link has expired. Please request a new one.")

    user = db.query(User).filter(User.id == reset_token.user_id).first()
    if not user or not user.is_active:
        raise HTTPException(status_code=400, detail="Invalid or expired reset token.")

    user.hashed_password = get_password_hash(data.new_password)
    reset_token.is_used = True
    db.commit()

    return {"message": "Password reset successfully. You can now log in with your new password."}


# ── Validate invite token (used by frontend to pre-fill email) ─────────────────

@router.get("/validate-invite/{token}")
def validate_invite(token: str, db: Session = Depends(get_db)):
    """
    Check if an admin invite token is valid and return the invited email (if set).
    Called by the frontend when the register page loads with ?invite=<token>.
    """
    now = datetime.now(timezone.utc)
    invite = db.query(AdminInviteToken).filter(AdminInviteToken.token == token).first()

    if not invite:
        raise HTTPException(status_code=400, detail="Invalid invite token.")

    expires_at = invite.expires_at
    if expires_at.tzinfo is None:
        expires_at = expires_at.replace(tzinfo=timezone.utc)

    if invite.is_used:
        raise HTTPException(status_code=400, detail="This invite has already been used.")
    if expires_at < now:
        raise HTTPException(status_code=400, detail="This invite has expired.")

    return {
        "valid": True,
        "invited_email": invite.invited_email,
        "expires_at": invite.expires_at,
    }
