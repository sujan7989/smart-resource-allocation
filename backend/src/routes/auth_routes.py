import secrets
import os
from datetime import datetime, timedelta, timezone
from fastapi import APIRouter, Depends, HTTPException, Request, Response, status
from sqlalchemy.orm import Session
from src.database import get_db
from src.models.user import User, UserRole
from src.models.token import PasswordResetToken, AdminInviteToken
from src.schemas.user import (
    UserCreate, UserLogin, UserResponse,
    ForgotPasswordRequest, ResetPasswordRequest,
)
from src.auth import verify_password, get_password_hash, create_access_token, get_current_user
from src.config import settings
from src import email_service

router = APIRouter(prefix="/api/auth", tags=["Authentication"])

COOKIE_NAME = "access_token"


def _is_production() -> bool:
    return bool(
        os.getenv("RENDER")
        or os.getenv("RAILWAY_ENVIRONMENT")
        or os.getenv("DYNO")
        or os.getenv("FLY_APP_NAME")
    )


def _set_auth_cookie(response: Response, token: str) -> None:
    response.set_cookie(
        key=COOKIE_NAME,
        value=token,
        httponly=True,
        secure=_is_production(),
        samesite="lax",
        max_age=settings.ACCESS_TOKEN_EXPIRE_MINUTES * 60,
        path="/",
    )


def _clear_auth_cookie(response: Response) -> None:
    response.delete_cookie(key=COOKIE_NAME, path="/")


# ── Register ───────────────────────────────────────────────────────────────────

@router.post("/register", response_model=UserResponse, status_code=status.HTTP_201_CREATED)
def register(
    request: Request,
    response: Response,
    user_data: UserCreate,
    db: Session = Depends(get_db),
):
    """
    Public registration for volunteer, field_worker, and admin (with invite token).

    - volunteer / field_worker: open registration, no token needed.
    - admin: requires a valid invite_token generated by the current admin.
      Only one admin is allowed at a time; the invite is single-use.

    Rate limited: 5 requests/minute per IP.
    """
    # ── Admin registration via invite token ───────────────────────────────────
    if user_data.role == UserRole.ADMIN:
        if not user_data.invite_token:
            raise HTTPException(
                status_code=400,
                detail="An invite token is required to register as admin. "
                       "Ask the current admin to generate one.",
            )

        now = datetime.now(timezone.utc)
        invite = (
            db.query(AdminInviteToken)
            .filter(AdminInviteToken.token == user_data.invite_token)
            .first()
        )

        if not invite:
            raise HTTPException(status_code=400, detail="Invalid invite token.")

        # Make expires_at timezone-aware for comparison
        expires_at = invite.expires_at
        if expires_at.tzinfo is None:
            expires_at = expires_at.replace(tzinfo=timezone.utc)

        if invite.is_used:
            raise HTTPException(status_code=400, detail="This invite token has already been used.")
        if expires_at < now:
            raise HTTPException(status_code=400, detail="This invite token has expired.")
        if invite.invited_email and invite.invited_email.lower() != user_data.email.lower():
            raise HTTPException(
                status_code=400,
                detail="This invite token was issued for a different email address.",
            )

        # Enforce single-admin rule
        existing_admin = db.query(User).filter(User.role == UserRole.ADMIN).first()
        if existing_admin:
            raise HTTPException(
                status_code=400,
                detail="An admin account already exists. "
                       "The current admin must transfer or delete their account first.",
            )

        # Mark invite as used
        invite.is_used = True

    # ── Volunteer / field_worker open registration ────────────────────────────
    existing = db.query(User).filter(User.email == user_data.email).first()
    if existing:
        raise HTTPException(status_code=400, detail="Email already registered.")

    new_user = User(
        email=user_data.email,
        full_name=user_data.full_name.strip(),
        hashed_password=get_password_hash(user_data.password),
        role=user_data.role,
        phone=user_data.phone,
        location=user_data.location,
    )
    db.add(new_user)
    db.commit()
    db.refresh(new_user)

    # Send welcome email (non-blocking — failure is logged, not raised)
    email_service.send_welcome_email(new_user.email, new_user.full_name, new_user.role.value)

    token = create_access_token(data={"sub": new_user.id, "role": new_user.role.value})
    _set_auth_cookie(response, token)
    return UserResponse.from_orm(new_user)


# ── Login ──────────────────────────────────────────────────────────────────────

@router.post("/login", response_model=UserResponse)
def login(
    request: Request,
    response: Response,
    credentials: UserLogin,
    db: Session = Depends(get_db),
):
    """
    Login with email and password. Sets a httpOnly cookie.
    Rate limited: 10 requests/minute per IP.
    """
    user = db.query(User).filter(User.email == credentials.email).first()
    if not user or not verify_password(credentials.password, user.hashed_password):
        raise HTTPException(
            status_code=status.HTTP_401_UNAUTHORIZED,
            detail="Incorrect email or password.",
        )
    if not user.is_active:
        raise HTTPException(
            status_code=403,
            detail="Account is inactive. Contact your administrator.",
        )

    token = create_access_token(data={"sub": user.id, "role": user.role.value})
    _set_auth_cookie(response, token)
    return UserResponse.from_orm(user)


# ── Logout ─────────────────────────────────────────────────────────────────────

@router.post("/logout", status_code=status.HTTP_204_NO_CONTENT)
def logout(response: Response):
    """Clear the auth cookie. Safe to call even with an expired token."""
    _clear_auth_cookie(response)


